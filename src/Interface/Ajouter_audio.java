    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        if(id_m!=-1){
        sound = new Audio();
        try {
            sound.set_id(getNextId("AUDIO"));
            sound.set_legende(jTextArea1.getText());
            sound.set_monument(id_m);
            sound.set_nom(jLabel4.getText());
            sound.save(PATH);
            JOptionPane.showMessageDialog(this,"Video Ajout√©e");
            this.dispose();
        
        } catch (SQLException ex ) {
            Logger.getLogger(Ajouter_audio.class.getName()).log(Level.SEVERE, null, ex);
        }   catch (Exception ex) {
                Logger.getLogger(Ajouter_audio.class.getName()).log(Level.SEVERE, null, ex);
            }
        
        }
        else{
            JOptionPane.showMessageDialog(this,"Monument non charg√©");
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

